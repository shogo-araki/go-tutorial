#!/usr/bin/env bash
# =========================================================
# コンテナ作成後に自動実行されるセットアップスクリプト
#
# devcontainer.json の postCreateCommand から呼ばれる。
# 「毎回手動でやる作業」をここに書いておくと、
# 環境構築の再現性が保たれる（実務でも同じ考え方を使う）。
# =========================================================
set -e

echo "=========================================="
echo " Go × Gin 学習環境をセットアップしています..."
echo "=========================================="

cd /workspace || exit 1

# Go Modulesの初期化はChapter1で手動体験してもらうため、
# ここでは自動生成しない（学習効果を優先）。
# 既にgo.modがある場合のみ依存関係を取得する。
if [ -f "go.mod" ]; then
    echo "[post-create] go.mod を検出しました。依存関係を取得します。"
    go mod download
fi

echo "[post-create] Air のバージョン確認:"
air -v || echo "Air のインストールに失敗している可能性があります"

echo "=========================================="
echo " セットアップ完了。"
echo " docs/chapter0-environment.md から学習を始めてください。"
echo "=========================================="
